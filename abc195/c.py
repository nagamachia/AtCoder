import math
N = int(input())

p = math.log10(N)
ans=0

if p<3:
    ans=0
elif p<6:
    ans=N-10**3+1
elif p<9:
    ans=(10**6-10**3)+(N-10**6+1)*2
elif p<12:
    ans=(10**6-10**3)+(10**9-10**6)*2+(N-10**9+1)*3
elif p<15:
    ans=(10**6-10**3)+(10**9-10**6)*2+(10**12-10**9)*3+(N-10**12+1)*4
else:
    ans=(10**6-10**3)+(10**9-10**6)*2+(10**12-10**9)*3+(10**15-10**12)*4+5

print(ans)